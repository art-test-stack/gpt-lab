    if not enwik8_local_path.exists():
        log0(f"Downloading enwik8 to {enwik8_local_path_zip}", logger=logger)
        import requests
        response = requests.get(enwik8_url)
        with open(enwik8_local_path_zip, "wb") as f:
            f.write(response.content)
        with zipfile.ZipFile(enwik8_local_path_zip, "r") as zip_ref:
            zip_ref.extractall(base_dir)
        log0(f"Unzipped enwik8 to {enwik8_local_path}", logger=logger)
        enwik8_local_path_zip.unlink()
        log0(f"Removed {enwik8_local_path_zip}", logger=logger)
    else:
        log0(f"Using existing enwik8 at {enwik8_local_path}", logger=logger)
    return enwik8_local_path

